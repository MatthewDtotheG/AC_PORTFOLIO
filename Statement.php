        <h2>PERCEPTION 
        <br>AND CONCEPTION</h2>

        <p>
            By studying methods of producing optical illusions, and the psychological and physiological mechanisms that create them, I have developed a better understanding of how I construct my reality. These paintings use photographs as recordings taken in the American Southwest to create a new experience that affects my memory of New Mexico and Arizona. This separation from a former reality develops my current perceptions, and the memory is experienced as an incomplete recording of the previous. These images are presented to the viewer with or without prior knowledge of the area, leading into a new memory. The link between mathematics, science, and art enables the viewer to immerse his or her self in an alternate form of reality.
        </p>
